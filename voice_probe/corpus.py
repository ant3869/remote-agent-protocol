"""The probe corpus -- a deliberate, wide-range catalog of text prompts.

Every prompt is a stand-in for a spoken request. Expectations are grounded in
the *actual* routing logic (``intent_router`` + ``voice_commands`` + the
session confirmation gate), traced tier by tier, so a mismatch is real signal
about the system rather than a mis-specified test.

Reading the ``expect_outcome`` column:

* ``chat``     -- should stay conversation; nothing dispatched.
* ``dispatch`` -- should hand a task to an agent with no confirmation.
* ``confirm``  -- should be HELD for a spoken/GUI yes-or-no before running
  (destructive verbs, installs, ungrounded mutations, vague capabilities).

``classifier_dependent=True`` marks cases the deterministic tiers (1-5) cannot
decide -- their correct outcome depends on the tier-6 Ollama classifier, so
they are only judged for real under ``--classifier live``.

Several cases deliberately encode a *known or suspected weakness* (an
unconfigured named agent, mid-sentence prompt injection, unicode-styled
destructive verbs, ...) or guard a *fixed* one (whole-word destructive matching
so "empty the recycle bin" gates while "installer" does not, informational
"how to delete X" lookups staying un-gated). Those carry a ``note`` explaining
the trap; when they fail they point at exactly the blind spot to fix.
"""

